# Definição da idade
idade = 10

# Operador ternário que verifica se a idade é maior ou igual a 16
# Se for verdadeiro (idade >= 16), o resultado é 'Voto Permitido'
# Se for falso, o resultado é 'Voto não Permitido'
resultado = 'Voto Permitido' if idade >= 16 else 'Voto não Permitido'

# Impressão do resultado
print(resultado)


